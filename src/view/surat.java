        private int idUser(String username) throws SQLException{
        int id_User = 0;
        ResultSet rs = koneksi.GetData("select id_user from admin where username = '"+username+"'");
        while(rs.next()){
            id_User = rs.getInt("id_user");
        
        }
        return id_User;
    }
        private void getTableSurat() throws SQLException{
        String sql = "select a.nama_lengkap as NAMA_LENGKAP, b.nik as NIK, b.keterangan as KETERANGAN, b.keperluan as KEPERLUAN from master a join surat_surat b on a.nik = b.nik";
        ResultSet set = koneksi.GetData(sql);
        DefaultTableModel model = (DefaultTableModel)tableSurat.getModel();
        model.setRowCount(0);
        try {
            while(set.next()){
                model.addRow(new Object[]{
                    set.getString("NAMA_LENGKAP"),
                    set.getLong("NIK"),
                    set.getString("KETERANGAN"),
                    set.getString("KEPERLUAN")                    
                    });
                }
            }catch(SQLException ex){
            System.out.println(ex);
            }
        }
        
