    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        String NIKe = nik_surat.getText();
        ResultSet rs = koneksi.GetData("select NAMA_LENGKAP, TANGGAL_LAHIR, ALAMAT from master where NIK ='"+NIKe+"'");
        try {
            while (rs.next()){
                nama_surat.setText(rs.getString("NAMA_LENGKAP"));
                tgl_surat.setText(rs.getString("TANGGAL_LAHIR"));
                alamat_surat.setText(rs.getString("ALAMAT"));
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(surat.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cariActionPerformed

