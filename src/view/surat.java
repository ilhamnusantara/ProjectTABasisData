    private void simpan_suratActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpan_suratActionPerformed
        String nik = nik_surat.getText();
        String keterangan = keterangan_surat.getText();
        String keperluan = keperluan_surat.getText();
        int id_user = 0;
        try {
            id_user = idUser(jComboBoxAdmin.getSelectedItem().toString());
        } catch (SQLException ex) {
            Logger.getLogger(master.class.getName()).log(Level.SEVERE, null, ex);
        }
        controller.InsertSurat(id_user, nik, keterangan, keperluan);
        try {
            getTableSurat();
        } catch (SQLException ex) {
            Logger.getLogger(master.class.getName()).log(Level.SEVERE, null, ex);
        }
        nik_surat.setText("");
        nama_surat.setText("");
        tgl_surat.setText("");
        alamat_surat.setText("");
        keterangan_surat.setText("");
        keperluan_surat.setText("");
    }//GEN-LAST:event_simpan_suratActionPerformed

